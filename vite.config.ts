import { defineConfig } from "vite";
import { svelte } from "@sveltejs/vite-plugin-svelte";
import { viteStaticCopy } from "vite-plugin-static-copy";
import zipPack from "vite-plugin-zip-pack";
import { resolve } from "path";

const isWatch = process.argv.includes("--watch");
const mode = process.env.NODE_ENV || "production";
const isTest = !!process.env.VITEST;

// Custom plugin to handle SVG imports as URLs
function svgUrlPlugin() {
  return {
    name: 'svg-url',
    transform(code: string, id: string) {
      if (id.endsWith('.svg')) {
        // Return the SVG content as a data URL
        const svgContent = code;
        const base64 = Buffer.from(svgContent).toString('base64');
        const dataUrl = `data:image/svg+xml;base64,${base64}`;
        return {
          code: `export default ${JSON.stringify(dataUrl)}`,
          map: null
        };
      }
    }
  };
}

export default defineConfig({
  plugins: [
    svgUrlPlugin(),
    svelte(),
    viteStaticCopy({
      targets: [
        { src: "plugin.json", dest: "./" },
        { src: "README.md", dest: "./" },
        { src: "icon.png", dest: "./" },
        { src: "preview.png", dest: "./" },
        { src: "assets", dest: "./" },
        { src: "src/assets/icons", dest: "./assets" },
      ],
    }),
    ...(!isWatch
      ? [
          zipPack({
            inDir: "./dist",
            outDir: "./",
            outFileName: "package.zip",
          }),
        ]
      : []),
  ],
  resolve: {
    alias: {
      "@": resolve(__dirname, "src"),
      ...(isTest
        ? {
            siyuan: resolve(__dirname, "src/__tests__/siyuan-stub.ts"),
            // Use real rrule library in tests
          }
        : {}),
    },
  },
  build: {
    outDir: "dist",
    emptyOutDir: true,
    minify: mode === "production",
    sourcemap: mode === "development" ? "inline" : false,
    lib: {
      entry: resolve(__dirname, "src/index.ts"),
      fileName: "index",
      formats: ["cjs"],
    },
    rollupOptions: {
      external: ["siyuan"],
      output: {
        entryFileNames: "index.js",
        assetFileNames: (assetInfo) => {
          if (assetInfo.name === "style.css") {
            return "index.css";
          }
          return assetInfo.name;
        },
      },
    },
  },
});
